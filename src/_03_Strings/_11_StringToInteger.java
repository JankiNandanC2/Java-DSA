package _03_Strings;

import java.util.Scanner;

public class _11_StringToInteger {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
//        System.out.println(" "+n);
        String s = "";
        s = s+n;
        System.out.println(s);
    }
}
