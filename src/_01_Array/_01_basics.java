package _01_Array;

public class _01_basics {
    public static void main(String[] args) {
        int[] arr = new int[5];
        arr[0]=1 ;
        arr[1]=10 ;
        arr[2]=100;
        arr[3]=1000 ;
        arr[4]=10000 ;
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
        System.out.println(arr[3]);
        System.out.println(arr[4]);
        arr[0]=10000 ;
        arr[1]=1000 ;
        arr[2]=100;
        arr[3]=10 ;
        arr[4]=1 ;
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
        System.out.println(arr[3]);
        System.out.println(arr[4]);
    }
}
